# 0'dan 4'e kadar sayıları yazdırma (5 dahil değil)
print("0-4 arası:")
for i in range(5):
    print(i)

print("-" * 20)

# 1'den 10'a kadar 2'şer artarak
print("1-10 arası tek sayılar:")
for i in range(1, 10, 2):
    print(i)

print("-" * 20)

