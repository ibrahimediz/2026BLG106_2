sayilar = [10, 5, 20, 15]

# Ekleme
sayilar.append(30)
print("Eklendi:", sayilar)

# Araya ekleme
sayilar.insert(1, 50) # 1. indekse 50 ekle
print("Araya eklendi:", sayilar)

# Çıkarma
sayilar.remove(5)
print("Silindi (5):", sayilar)

# Sıralama
sayilar.sort()
print("Sıralandı:", sayilar)

# Tersten sıralama
sayilar.reverse()
print("Ters çevrildi:", sayilar)
